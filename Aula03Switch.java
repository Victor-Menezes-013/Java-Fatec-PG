package aula;

public class Aula03Switch {

	public static void main(String[] args) {
		int op = 2;
		switch(op) {
		case 1:
			System.out.println("Opção 1 foi a escolhida");
			break;
		case 2:
			System.out.println("Opção 2 foi a escolhida");
			break;
		case 3:
			System.out.println("Opção 3 foi a escolhida");
			break;
		default:
			System.out.println("Opção inválida");
		}

	}

}
