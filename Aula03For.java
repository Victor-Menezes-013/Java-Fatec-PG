package aula;

public class Aula03For {

	public static void main(String[] args) {
		
		System.out.println("Incremento....");
		for (int i = 0; i < 5; i++) {
			System.out.println("i = " + i);
		}
		
		System.out.println("Decremento....");
		for (int i = 5; i > 0; i--) {
			System.out.println("i = " + i);
		}

	}

}
